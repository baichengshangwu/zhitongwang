    let grad=ctx.createLinearGradient(0,pad.t,0,pad.t+ph);grad.addColorStop(0,'rgba(139,92,246,0.35)');grad.addColorStop(1,'rgba(139,92,246,0.02)');
    ctx.beginPath();ctx.moveTo(pad.l,pad.t+ph-((pts[0]-min)/rng)*ph);
