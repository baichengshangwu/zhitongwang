// Get all provinces
function getProvinces() { return Object.keys(CN_REGIONS); }

// Get cities under a province
function getCities(province) {
  var p = CN_REGIONS[province];
  return p ? Object.keys(p.cities) : [];
}

// Get counties under a city
function getCounties(province, city) {
  var p = CN_REGIONS[province];
  if (!p) return [];
  return p.cities[city] || [];
}
